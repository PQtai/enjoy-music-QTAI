import {
    SET_IS_PLAYSONG,
    SET_SONG_PROGRESS,
    SET_INDEX_SONG
}from './constants'

export const setIsPlaying = payload => ({
    type: SET_IS_PLAYSONG,
    payload: payload
});
export const setSongProgress = payload => ({
    type: SET_SONG_PROGRESS,
    payload: payload
});
export const setIndexSong = payload => ({
    type: SET_INDEX_SONG,
    payload: payload
});